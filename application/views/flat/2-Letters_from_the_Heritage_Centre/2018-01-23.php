			<p>The turbine was installed at the top of the caisson, and measurements began. The initial results were disappointing. But subsequently, different turbine configurations were developed and tested, and a successful technology emerged. The wave energy plant began producing power steadily.</p>
			<p>Eventually the wave energy project led to the birth of a new organisation entirely devoted to technologies related to the ocean, the National Institute of Ocean Technology (NIOT). IIT Madras researchers continued to work on the wave energy project in collaboration with their NIOT counterparts.</p>
			<p>The wave energy plant at Vizhinjam was run for many years, during which period the electricity it produced was used to desalinate sea water. The drinking water produced by the desalination plant was supplied to the local community.</p>
			<p>For the exhibition at the Heritage Centre, we got from NIOT a beautiful threedimensional model of the wave energy plant at Vizhinjam. NIOT also lent us the first turbine that had been tested at the caisson and a three-dimensional model of a wave energy device they were developing for powering navigation buoys. From the Department of Ocean Engineering we got a 1:10 model of the great caisson that had been installed in the sea at Vizhinjam. This was an exotic-looking affair, made of thick-walled acrylic blocks held together by metal screws. The model caisson had been fabricated in West Germany in the early days of the wave energy project. It had been placed in a wave flume and parameters such as the air pressure and velocity measured.</p>
			<p>The turbine loaned by NIOT was a full 2 metres in diameter. It was displayed outside the Heritage Centre, near the entrance. Casual viewers could have assumed the contraption was a windmill, which it was in a way.</p>
			<p>The model of the caisson was very unwieldy. A forklift truck and a number of helpers brought it over from the Department of Ocean Engineering. The model was slid on rollers into the middle of the Heritage Centre, and that is where it throughout the exhibition and the better part of the following year.</p>
			<p>The Department of Ocean Engineering had given us the model to keep. Finally it was time for a change. The space at the centre of the hall was needed. We decided to place the caisson model outside the Heritage Centre, on the lawn near the entrance, where the windmill-turbine had stood.</p>
			<p>Taking the caisson model out of the Heritage Centre turned out to be a greater challenge than bringing it in had been. Ten able-bodied assistants struggled to move it. There was much shouting of directions and encouragement as they strained to lift it on to a trolley. I began to think that someone would be injured or something damaged.</p>
			<p>But all was well in the end. The model was moved out, and it slid into place easily. Curiously shaped, exotic looking, it stands outside the Heritage Centre, drawing the attention of all who pass by, possibly mystifying them. I like to think that they are reminded of the Louvre, with its glass pyramid.</p>
