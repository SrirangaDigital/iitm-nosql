			<p>We have a few souvenirs of Cyclone Vardah at the Heritage Centre. They are roughly of a form that a mathematical textbook might describe as a vertical cylinder of circular cross-section. The shorter of these cylinders are high enough to reach your waist. And their girth is such that if you wrapped your arms around one, your fingers might just touch. These are sections of trunks of trees, probably neem trees, that were toppled by Cyclone Vardah in the campus.</p>
			<p>These sections were stripped of their bark, smoothed down and varnished by the carpentry unit of the Central Workshop. So well polished are they that they present the appearance of water-worn rocks. If you tried to lift them, your impression that they are rocks would be strengthened—they are surprisingly heavy. Actually, they are so heavy that you would need to be a trained professional to lift or move them.</p>
			<p>These pieces of wood could be the heaviest objects we have inside the Heritage Centre, barring some of the furniture. I believe they are heavier even than the stone laid at the inauguration of IIT Madras, on 31 July 1959. And that stone is set in a frame of concrete!</p>
