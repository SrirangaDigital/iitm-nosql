			<h1>The Heaviest of Them All</h1>
			<p class="text-right"><b>IIT Madras</b><br/>23 January 2018</p>
			<p>In December 2016, a tropical storm that originated near the Malay Peninsula glanced the Andaman and Nicobar Islands and went on to make landfall at Chennai. This was Cyclone Vardah. A Very Severe Cyclonic Storm according to the classification of the Indian Meteorological Department, Vardah weakened a little before it struck the city. Even so, the wind gusted most powerfully—the speeds are reported to have reached 120 kilometres per hour at the port. The cyclone caused devastation in the city, uprooting a large number of trees.</p>
			<p>Many trees fell in the IIT Madras campus too.</p>
