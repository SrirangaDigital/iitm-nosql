			<p>But a couple of years back, we had on display items that were probably even heavier. We had them here as part of a thematic exhibition on the wave energy project of IIT Madras.</p>
			<p>IIT Madras began working on this project in the 1980s. It was aimed at producing electricity from wave energy. There is abundant energy in ocean waves worldwide. It derives from wind energy, which in turn is fuelled by the sun. But surprisingly, there were few serious efforts to tap it prior to the 1970s.</p>
			<p>In 1973 there was an oil embargo. Oil prices shot up. Though the embargo was lifted after a few months, its effects lingered. The oil consuming countries began looking at energy from alternative sources. One of these sources was wave energy. A host of countries, notable among them Japan, the UK and Norway, initiated efforts to tap wave energy. A number of ingenious devices were conceived of.</p>
			<p>The technology IIT Madras developed involved the entrapment of a column of air above the surface of the sea. As the surface rose and fell with each wave, the column of air oscillated. This oscillating air column drove a turbine, producing electricity. This sounds simple, but in fact there was a lot of complex engineering involved.</p>
			<p>The project went from the lab to the ocean. A power plant was built at the end of a breakwater at Vizhinjam, near Trivandrum. Tremendous difficulties were faced in working in the marine environment.</p>
			<p>A 10 metre high concrete structure, known as a caisson, was built on the Vizhinjam beach and floated out into the sea like a small ship. It was made to settle on a bed of stone prepared on the floor of the sea.</p>
