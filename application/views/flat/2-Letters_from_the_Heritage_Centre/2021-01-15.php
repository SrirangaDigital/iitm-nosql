			<p>Among the articles published in the <i>Philosophical Transactions of the Royal Society of London</i> in 1823 was one titled ‘Experiments for Ascertaining the Velocity of Sound, at Madras in the East Indies’. The author was a person named John Goldingham.</p> 
			<p>Goldingham conducted his study over many months, beginning in July 1820. He used the sound of guns that were fired daily at two places: “At Fort St. George (Madras) a morning and an evening gun are fired from the ramparts, as is customary in fortified places, the former at day light, and the latter at eight o’clock in the evening”, wrote Goldingham. “At St. Thomas’s Mount, the artillery cantonment, morning and evening guns are also fired, one at day light and the other at sun set.” Goldingham made a series of observations from the Madras Observatory, at Nungambakkam: “All the experiments were made with chronometers, which had 100 beats in 40 seconds, sometimes by three observers, myself and two of the Observatory Bramin assistants, but generally by two: the observers having repaired to the station at the top of the Observatory building, a little before the expected time, and each holding his chronometer so that he could distinctly hear the beats, began to count the instant he saw the flash, and continued counting till he heard the report; the number of beats between the flash and report was then immediately put down upon a slip of paper, by each observer, without communication with the others, and the papers delivered to me for their contents to be registered...”</p> 
			<p>Goldingham measured the distances with great care: “first, by a survey made for the purpose, a base having been measured, and the angles taken with a great circular instrument ... Secondly, by using two or three of Colonel Lambton’s distances and bearings found by the trigonometrical survey.”</p> 
			<p>Goldingham’s account tells us much about the level of advancement of science at the time that it was written. So too does it offer hints that transport us back in time to Madras as it was 200 years back—a practice of firing guns twice a day at the Fort and at St. Thomas Mount, the existence of an active observatory in the city, the construction of a new building at that observatory.</p> 
			<p>Goldingham’s publication carried a map that showed the relative positions and distances of the points. According to Goldingham, this map was “filled up from the best surveys, under the superintendance of E. Lake, Esq. of the Madras Engineers”, who he mentions is his son-in-law. I found the map engrossing. I thank Shyamal L. for drawing my attention to this map and thus to the article in which it appeared.</p> 
