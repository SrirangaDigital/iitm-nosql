			<h1 id="lfh-20210115-article2"><br />The mystery of the fallen potted plants</h1>
			<div class="row">
				<div class="col-md-2"></div>
				<div class="col-md-8 clear-paddings text-center">
					<img class='img-thumbnail' src='<?=STOCK_IMAGE_URL?>lfh/2021-01-15/004.JPG' />
				</div>
				<div class="col-md-2"></div>
			</div>
			<p>There was order and neatness in every aspect, on the lawn outside. The glass window of the Heritage Centre revealed a soothing view of greenery and sunlight. However, there was a slight concern at the Centre. Three pots amidst the uniform green, always seemed to be lying down. Funnily, they pointed to the same direction in their state of horizontal languor.</p>
			<p>Who could have knocked them down? The housekeeping staff never ventured out onto the lawn, unless it was to clean the glass window, which was a rare occurrence in itself, so that was ruled out. We decided that it was the wind that accompanied the unpredictable downpours, which was responsible for this breach in tidiness. The very next day, the pots were up again, vertically positioned, vigilant. Standing upright as though they hadn’t done anything wrong. The three plants reminded me of my college days, of a good friend of mine, who always sat next to me.</p>
			<p>He found the time to nap in class between classes, during classes, before classes and after classes. His head was always on the table, face flat, eyes shut, and body still as a rock. This was irrespective of the level of noise in class. He was similar to the fallen plants in the sense that, when someone woke him up, usually me, when the teacher walked in, he would rise up straight and turn his face toward the front of the class where the teacher stood. There would be no trace of the fact that he was in a completely different dimension just a second ago. Similarly, the plants, on assuming the vertical position again, bore the mark of innocence, much like my friend, who now is an assistant professor at a university. I don’t think he can continue his old practice of napping during classes now, unless he manages to put his students to sleep, following which he will be free to do the same, now that he has a table for himself.</p>
			<p>Coming back to the mysterious plants at our Heritage Centre, we forgot about the incident until one fine, sunny day we saw the plants flat on the ground, yet again. I do not know which god they were paying obeisance to, but it seemed that they were deeply engaged in a prayer of sorts. Yet again, they had chosen to face the same direction as they lay there. On second thoughts, I thought this was a protest of some sort. Maybe they did not get along well with the other plants in the hood? Were they discriminated or threatened, or was their share of water and sunlight being stolen by the neighbourhood plant bully? Or even worse, was there a plant battle at night, where there was a shootout, and our three lucky musketeers had ducked just in time? It all seemed possible, given the enigmatic occurrences that were likely to happen in the forest of IIT Madras, long after we had gone to sleep.</p>
