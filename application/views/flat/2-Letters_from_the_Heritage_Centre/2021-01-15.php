			<h1 id="lfh-20210115-article1">The campus and the velocity of sound</h1>
			<p class="text-right"><b>IIT Madras</b><br/>15 January 2021</p>
			<p>The picture alongside shows a part of a Madras City bus route map published in 1966. Areas such as Adyar and Saidapet are marked, whereas IIT Madras (more than 6 years old then), CLRI, the College of Engineering Guindy/Anna University and Raj Bhavan are not. Two water bodies, the larger one marked ‘Tank’, are shown occupying much of the IIT campus. These had presumably been connected with a third water body, lying to the north, marked as ‘Katteri Eri’, and had subsequently been separated by the Sardar Patel Road (then known as Elliot Beach Road). A comparison with a contemporary map will show the extent to which the road network in the area has grown in the last 50 years or so. The map was published by the Madras State Transport Department and is in the collection of Mr. Thomas Tharu (‘Tee Square’), alumnus of the 1969 batch of IIT Madras, who kindly allowed this photograph to be taken for the Heritage Centre</p>
