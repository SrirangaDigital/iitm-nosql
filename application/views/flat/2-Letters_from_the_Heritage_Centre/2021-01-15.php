			<p>The College of Engineering (Anna University) is also not marked on the map. This is only to be expected. Although the precursor of this institution, the School of Survey, was established in 1794, the College of Engineering would move to the present location of Anna University only in 1920.It would seem that Audyaur was an unremarkable hamlet, for it is not connected to the rest of the city by any road. There was none worth marking on the map anyway.</p>
			<p>And finally, the future IIT campus:: no feature is marked on it except for a water body that occupies a good part of it and space outside, one that bears no name.</p>
