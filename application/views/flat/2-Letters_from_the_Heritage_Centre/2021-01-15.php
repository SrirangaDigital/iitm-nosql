			<p>A few days later, the rain came down hard. We were watching the blackbuck and spotted deer galloping around looking for shelter. A corpulent fellow, a spotted deer, that is, had made himself comfortable outside the window. He had located a dry spot. He showed no signs of getting up and leaving as the rain got heavier. As the rain subsided, he got up, shook himself and walked along the length of our glass window outside the building and stepped onto the lawn. As he did so, he knocked down one, two, and three pots down, since there was no space for him to move leisurely. He casually knocked them down, nonchalant and unfettered by the protests of the innocent green bystanders.</p>
