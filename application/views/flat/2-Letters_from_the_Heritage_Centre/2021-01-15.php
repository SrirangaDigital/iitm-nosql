			<p>There are several points about the map that one finds striking simultaneously. The city is of course much smaller and sparsely built over compared with the present. The road network appears ludicrously rudimentary. One cannot even find a road along the Marina. That having been said, many of the roads that are shown seem to have been lined entirely with trees, including Mount Road and Poonamallee High Road. Naturally, there are no railway lines or the Buckingham Canal—these would come up later.</p>
			<p>Places such as Besant Nagar and T. Nagar are not marked on the map—these places were non-existent when the map was made. Indeed, at the present situation of T. Nagar is a large water body, one bearing the name ‘St. Thome Tank’, and close to it is the ‘Long Tank’. On the other hand, the map does have ‘Race Stand’, ‘Moubray’s Cupola’ and ‘Brodia Castle’ marked on it.</p>
			<p>And the spellings of place-names! We have Peeramboor, Inaveram, Porshewaukam, Chindadrepeta, Ellemboor and Tondiaved, which we can relate to current names. But what was the ‘Powder Mills’? An explosives factory? And what was ‘Attapol’, pray?</p>
			<p>Of special interest to us of course are ‘Audyaur’ and ‘Gindy’, to the vicinity of which our eyes are drawn as we search for the place that would become the IIT Madras campus.</p>
			<p>In November 1821 (a month in which Goldingham was still making his measurements, six months before he wrote up his article in the <i>Philosophical Transactions</i>), the government bought three contiguous properties at Guindy (the ‘Guindy Forest’), as a result of which it had some 1000 acres for development into the residential estate of the Governors of Madras. According to S. Muthiah’s book on the Raj Bhavans of Tamil Nadu, at this point there were three single-storeyed buildings at the place where the present Raj Bhavan stands. Presumably these are the buildings shown as a set of small constructions at ‘Gindy’ on the map.</p>
			<p>There is no Highways Research Station marked on the map—it would have been very surprising if it <i>had</i> been marked because the HRS was established in 1954! There is a sharp bend in the course of the Adyar River in this region, and just outside this bend is an unnamed plot, marked in dotted lines on the map.</p>
