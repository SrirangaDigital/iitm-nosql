			<p>I went over to Mandak to give it a dekko recently. The old hostel was painted in off-white and watery blue. Additional rooms had been constructed above the common room and mess at some time and all the rooms renumbered so that 221, where I had stayed, was now 244. On the whole, the hostel had grown quite faded and staid.</p>
			<p>I took a picture of Mandak from the terrace of Ganga Hostel to compare the view today with the old picture. How different everything is! It was all so different that you could have conducted a ‘spot the similarities’ contest with the two photographs, the old and the new!</p>
			<p>The area around the hostel was bare in 1968; so much vegetation has come up around it since that you can hardly see Mandak now. And the neighbourhood of the campus has changed too—a great many tall buildings have sprung up.</p>
			<p>But do you know what, there is a constant in the two pictures: a single date palm tree. This is seen quite clearly in the older photograph, standing near a common-room window. In the recent picture too, there is a date palm at the same location, but you can just see the crown of this date palm, and that if you look closely. It has to be the same tree, hasn’t it?</p>
