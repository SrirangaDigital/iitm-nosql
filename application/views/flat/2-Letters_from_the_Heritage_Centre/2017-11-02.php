			<p>The tree in the old photograph seems to have reached its full height. So just how old was it at the inauguration of the hostel. Is it a hundred years old now?</p>
			<p>Somehow, it is a happy thought that an ancient tree has been standing near Mandak for a half-century, watching thousands of students come and go.</p>
