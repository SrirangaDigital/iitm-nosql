			<p>Since we are on the theme of traditions, it would not be out of place to mention that it was customary for Mandakini Hostel to have a somewhatunconventional painting scheme. My recollection of the good old hostel is that it sported a bold red-and-white pattern, reminiscent of south Indian temples or a certain brand of toothpaste, depending on your point of view. Campastimes informs us that when Mandakini Hostel was inaugurated in 1968, it was ‘dressed in grey and pink’. You could not say that by looking at the early picture we have in the archives though because ours is a blackand-white photograph.</p>
