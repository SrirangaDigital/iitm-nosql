			<p>Mandakini Hostel, just shy of its 50th ‘birthday’, is to be replaced by a new structure that can accommodate 1200 students.</p>
			<p>‘Mandak’ was the last of the first set of hostels at IIT Madras, built in the 1960s. It was inaugurated on 18 August 1968 by Mrs. Sushila Ramachandran, wife of Director A. Ramachandran. Alakananda Hostel, the closest ‘sibling’, had been inaugurated three years previously, on 20 July 1965. It would be another 12 years or so before another hostel was built on campus.</p>
			<p>Geographically speaking too, Mandakini was the last hostel, being located at the end of the hostel avenue, in the south-eastern corner of the campus.</p>
			<p>A satellite image of the campus taken some time after the completion of Ganga and Jamuna and before the construction of Alakananda shows that the Mandak site was a rather damp corner—there are two ponds that are decidedly close to it. So it is hardly surprising that Mandakini was affected more than any other hostel by the December 2015 deluge.</p>
			<p>I stayed in Mandak in 1981–1982 as a fresher, as did most of my batch mates —the first group of students of the 4-year B.Tech. programme. The previous year too, the ‘freshies’—those of the last batch of 5-year B.Tech. students— had been accommodated in Mandakini Hostel. Incidentally, Dr. Chandramouli Visweswariah of that batch made available to the Heritage Centre copies of the wall magazine that they brought out in 1981, The Mandak Rag. These we have digitised.</p>
			<p>As it happens, Mandakini had always been a freshman hostel. Prof. Ramani, of the Department of Humanities, who was the warden of the hostel from 1973 to 1975, says that from the very beginning, Mandakini Hostel was for accommodating first-year students.</p>
			<p>But the freshman tradition of Mandakini Hostel ended in the 1980s. The 1987 B.Tech. batch were the last set of students to stay at Mandakini in their first year (1983–1984). Actually, some students of that batch stayed backed in Mandak and continued to be accommodated there right through their 4- year course at IIT Madras.</p>
			<p>Talking of traditions, Mandakini Hostel had a beautiful garden, tended with care by the gardener Mr. Kondaiah, in the early 1980s. Every year, this garden took part in an annual garden competition which was, I believe, conducted by the Madras Horticultural Society. Suffice it to say that Mandak won the first prize with monotonous regularity.</p>
