			<p>Some months back, we added to the photographic collections of the Heritage Centre a good number of pictures taken by Mr. C. Gourisankar, who photo-documented events and infrastructural developments at IIT Madras over a long time, starting from the earliest days.</p>
			<p>I was browsing through the newly digitised Gourisankar collection when I started flipping through an album titled &lsquo;Opening of School, 8-7-1963&rsquo;. This was Album 208.</p>
			<p>I wondered at the many stories it tells. Album 208 tells you of course that Vanavani School was declared open on a Monday 54 years ago by Dr. A. Lakshmanaswami Mudaliar, first Chairman, Board of Governors, IIT Madras. The album also tells you that Father Murphy, from Loyola College, played an important role in the newly formed school. It tells you that Prof. Sengupto, first Director of IIT Madras, Mrs. Shanti Sengupto, Mr. R. Natarajan, first Registrar of IIT Madras, and a number of German professors and technicians participated in the opening ceremony. It tells you that the first students, little children, were treated to cool drinks on the first day of school and that they used gaily striped paper straws.</p>
			<p>&nbsp;</p>
