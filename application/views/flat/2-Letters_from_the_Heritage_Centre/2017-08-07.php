			<p>I came to picture IMG_0206, which showed the Chief Security Officer, Mr. Venkatraman, holding up a gong and Dr. Mudaliar striking it with a mallet, no doubt to start classes. Dr. Mudaliar was lifting the mallet with his left hand. This was interesting, I thought, remembering that only 10 per cent of the world is left handed. By what must have been a great coincidence, the security officer was left handed too! He was using his left hand to hold up the gong! I scrutinised his elaborate uniform and saw that on his lapel were the metal letters 'T.I.I'. I wondered what the full name of this agency was.</p>
