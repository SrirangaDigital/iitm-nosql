			<p>Mystery solved. There was no extraordinary prevalence of left-handedness among the dignitaries on that day in 1963, no mistake in the floral decorations, no &lsquo;T.I.I&rsquo; on the uniform of the Security Officer&mdash;only &lsquo;I.I.T&rsquo;!</p>
