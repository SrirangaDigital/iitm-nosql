			<p>The picture bearing number IMG_0207 shows Dr. Mudaliar cutting a ribbon to open the new school building. Standing near him is Mr. Y.S. Ramaswamy, the Superintending Engineer of IIT Madras, apart from Father Murphy, Director Sengupto and Registrar Natarajan. There is a coir mat bearing a geometric pattern at the entrance. A 'WELCOME' created out of strings of flowers&mdash;it is not clear whether these are real or paper&mdash;hangs from above. Something about the picture struck me as being odd&mdash;shouldn&rsquo;t the WELCOME have faced outwards? It <i>should</i>have appeared inverted, but it was not. Strange, how this mistake had taken place, I thought.</p>
