			<p>But wait, something has happened, go back to the previous photograph please. Yes, the seating order on the stage is inverted! Did the speakers and dignitaries change places during the function? Surely, that could not be!</p>
			<p>Then the penny dropped&mdash;what we had received were Mr. Gourisankar&rsquo;s <i>negatives</i>, and during the course of digitisation, some of them had been scanned the &lsquo;wrong-side-up&rsquo;!</p>
