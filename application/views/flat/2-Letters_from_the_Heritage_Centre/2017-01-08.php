			<p>The portrait of Prof. Sengupto, at the entrance of the Heritage Centre, is the tallest item on display here. As I had mentioned in my previous letter, this painting is a re-creation of another, a portrait created many years ago that was damaged by the combined action of moisture and termites.</p>
			<p>When was the original painted? Who painted it? Who unveiled it? When? Where?</p>
			<p>A search for the answers to these questions was instructive not only about the painting itself but about the ways of history itself. One of the questions was readily answered by a search through the photographs at the Heritage Centre.</p>
			<p>There is one set of negatives entirely devoted to the unveiling of the portrait, and these pictures clearly show that the venue was the Open Air Theatre. It is also clear that the function was held after sunset for the sky is dark. Prof. A. Ramachandran, second Director of IIT Madras, Dr. A.L. Mudaliar, first Chairman, Board of Governors, and Prof. S. Sampath, first Deputy Director, are clearly recognisable.</p>
