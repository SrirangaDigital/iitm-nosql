			<p>And where was the painting kept in the institute until it was brought to the Heritage Centre? I have not been able to find the answers to these questions yet.</p>
			<p>As the English poet and novelist Thomas Hardy might have expressed it, questions arise, questions linger.</p>
			<p>Can any of our alumni from the batches of the late 1960s and early 1970s answer these questions? If yes, a clearer picture would emerge, so to say.</p>
