			<p>There are no details, however, on the negatives themselves, on the envelope containing the negatives or within the pictures (by way of a banner, for example).</p>
			<p>When was the event held? No mention of the event could be found in the Annual Number editions available at the Heritage Centre. It was also somewhat unlikely that the annual reports of the institute would have recorded the unveiling, and at any rate an exhaustive search would have been very time-consuming.</p>
			<p>Now, Prof. Ramachandran was Director of IIT Madras from December 1967 to May 1973. And Dr. A.L. Mudaliar was the Chairman, Board of Governors of the institute from January 1959 to January 1969. The period when they were both at IIT was thus just a little more than one year, corresponding to the calendar year of 1968. So the portrait was probably unveiled some time in 1968. This was good progress, but could a more specific date be found? And who was the gentleman in a suit who actually unveiled the painting?</p>
