			<p>Clearly, he was a distinguished guest of the institute. Also, one could hazard a guess from his appearance that he was not Indian.</p>
			<p>I went back to the annual reports. The 1967–1968 annual report did carry a list of important visitors to the institute. Among them were a host of German visitors.</p>
			<p>As it happens, the first of these visitors after Prof. Sengupto’s tenure as Director was Dr. Lehnartz, President of DAAD. How was one to obtain a photograph of Dr. Lehnartz and to determine if the person unveiling Prof. Sengupto’s portrait was him? A Google image search for ‘Dr. Lehnartz DAAD’ yielded a small photograph:</p>
			<p>&nbsp;</p>
			<div class="col-md-12 clear-paddings text-center">
				<img class='img-thumbnail' src='<?=STOCK_IMAGE_URL?>lfh/2017-01-08/0004.JPG' />
			</div>
			<p>&nbsp;</p>
			<p>This was Dr. Emil Lehnartz, President of DAAD from 1960 to 1968, and the photograph was on DAAD’s website. I thought that I did not need to look any further. But I was not absolutely certain. So I asked DAAD for confirmation.</p>
			<p>Soon enough, they wrote back to say that they had compared the pictures at the Heritage Centre with the photos they had of Prof. Lehnartz in their archive. They said that they were ‘pretty sure’ that it was the same person. They even sent a photo of Prof. Lehnartz taken at a meeting of DAAD <img>scholars in Travemünde, a German coastal resort on the Baltic Sea, in 1967:</p>
			<p>&nbsp;</p>
			<div class="col-md-12 clear-paddings text-center">
				<img class='img-thumbnail' src='<?=STOCK_IMAGE_URL?>lfh/2017-01-08/0005.JPG' />
			</div>
			<p>&nbsp;</p>
			<p>That clinched it—now two questions had been answered in one stroke: (1) the one about who had unveiled Prof. Sengupto’s painting and (2) the question about when the event at the Open Air Theatre had taken place—the 1967–1968 annual report documented Dr. Lehnartz’s visit to the institute as having taken place on 19 January 1968.</p>
			<p>But one thing leads to another. More questions arise. Who is the prominent person in the pictures in a dhoti, waistcoat and shawl?</p>
