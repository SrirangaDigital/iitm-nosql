            <h1>About</h1>
            <p>The Heritage Centre was formally inaugurated by Dr. Arcot Ramachandran, former Director IIT Madras on 3rd March 2006. The Centre is located on the ground floor of the administration building. The actual idea of a Heritage Centre was mooted in the year 2000 and it has become a reality due to the efforts of the Professor-in-charge Dr. Ajit Kumar Kolar and his team. The Centre will function as a repository of material of heritage value and historical significance of various facets of the Institute.</p>
            <p>The exhibits include photographs, documents, publications, paintings, portraits, products developed and other articles. Information regarding important events, laboratory development, visits of important dignitaries, Indo-German cooperative activities, and academic achievements of faculty and students also are included. Aspects of IITM campus features and development, campus life and student activities are also included, thus broadening the scope of the Centre in the future to non-academic activities also.</p>
            <p>The activities of the Heritage Centre will be of a continuous nature from now on and hence the support and cooperation of all IITians (students, faculty and staff, past and present) is very essential in making the Centre meet its goal of preserving IITM history and culture for the future generations of IITians. By the very nature of the task, the role of the Alumni is crucial in establishing and furtherance the Centre.</p>
